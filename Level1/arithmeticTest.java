//Illustration of all arithmetic concepts

public class arithmeticTest {
	public static void main(String[] args) {
		int n1 = 98;
		int n2 = 5;
		int sum, difference, product, qoutient, remainder;
		
		sum = n1 + n2;
		difference = n1 -n2;
		product = n1*n2;
		qoutient = n1/n2;
		remainder = n1%n2;
		
		System.out.println("The sum, difference, product, qoutient, remainder of " + n1 + " and " + n2 + " are " + sum + ", " + difference + ", " + product + ", " + qoutient + ", and " + remainder );
	}
}
