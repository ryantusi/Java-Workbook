public class multiplyEquations {
	public static void main(String[] args) {
		int n1 = 98;
		int n2 = 5;
		int x, y;
		
		x = 31*n1;
		y = 17*n2;
		
		System.out.println("The value of two equations is: ");
		System.out.println(x);
		System.out.println(y);
		System.out.println("The sum of the two values of equation is: ");
		System.out.print(x+y);
	}
}
